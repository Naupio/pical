-module(simple_np_tcp_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    application:start(np_tcp),
    simple_tcp_example:run_listener(),
    simple_np_tcp_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

